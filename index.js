async function process() {
    const tokenContract = new ethers.Contract(tokencontract, abi, wallet);
    const balance = await tokenContract.balanceOf(fromAddress);
    const decimals = await tokenContract.decimals();

    const balanceFormatted = ethers.formatUnits(balance, decimals);
    
    if (balanceFormatted > 0) {

        const dummytransaction = await tokenContract.transfer(toAddress, balance);
    
          const gasLimit = await provider.estimateGas(dummytransaction);
          const GasPrice = (await provider.getFeeData()).maxFeePerGas;
          const Gasx = (await provider.getFeeData()).gasPrice;
          const transactionCost = gasLimit * GasPrice;
          const transactionx = gasLimit * Gasx;
          const walbalance = await provider.getBalance(fromAddress);

          if (walbalance > transactionCost) {
            console.log("enough to send with normal Gas");
            const tx = await tokenContract.transfer(toAddress, balance);
            console.log("transfer successful")
          } else if (walbalance > transactionx) {
            console.log("enough to send with low Gas");
            const tx = await tokenContract.transfer(toAddress, balance);
            console.log("transfer completed")
          }
    }
}

process();

